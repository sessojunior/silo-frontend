// Função para calcular estatísticas dos projetos
export function calculateProjectStats(projects: Project[]) {
	const stats = {
		total: projects.length,
		active: projects.filter((p) => p.status === 'active').length,
		completed: projects.filter((p) => p.status === 'completed').length,
		paused: projects.filter((p) => p.status === 'paused').length,
		cancelled: projects.filter((p) => p.status === 'cancelled').length,
		avgProgress: 0,
	}

	if (projects.length > 0) {
		const totalProgress = projects.reduce((sum, project) => sum + project.progress, 0)
		stats.avgProgress = Math.round(totalProgress / projects.length)
	}

	return stats
}
